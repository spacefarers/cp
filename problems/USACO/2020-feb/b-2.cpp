void setIO(const string& name) {
    ios_base::sync_with_stdio(false); cin.tie(nullptr);
    freopen((name+".in").c_str(),"r",stdin);
    freopen((name+".out").c_str(),"w",stdout);
}

int main() {
    setIO("breedflip");
    int n;
    cin>>n;
    string a, b;
    cin>>a>>b;
    bool last_same=true;
    int ans=0;
    for (int i=0;i<n;i++){
        if (a[i]==b[i]){
            if (!last_same) {
                ans++;
                last_same = true;
            }
        } else {
            last_same=false;
        }
    }
    cout<<ans<<endl;
    return 0;
}